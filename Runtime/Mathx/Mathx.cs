using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

namespace SensenToolkit.Mathx
{
    public static class Mathx
    {
        public static int Sqr(int v)
        {
            return v * v;
        }
        public static float Sqr(float v)
        {
            return v * v;
        }
    }
}
